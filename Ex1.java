public class Ex1 {
	public static void main(String[] args){
		//Entrada da soma
		double numero1 = 10;
		double numero2 = 45;
		//Entrada da subtração
		double numero3 = 2019;
		double numero4 = 55;
		//Entrada da multiplicação
		double numero5 = 5;
		double numero6 = 6;
		//Entrada da divisão
		double numero7 = 20;
		double numero8 = 4;


		//soma
		double resultadoDaSoma = numero1 + numero2;

		//subtração
		double resultadoDaSubtracao = numero3 - numero4;

		//multiplicação
		double resultadoDaMultiplicacao = numero5 * numero6;

		//divisão
		double resultadoDaDivisao = numero7 / numero8;

		//Saída Soma
		System.out.println( "A soma: "+numero1+" + "+numero2+" = "+resultadoDaSoma);

		//Saída Subtração
		System.out.println( "A subtração: "+numero3+" - "+numero4+" = "+resultadoDaSubtracao);

		//Saída Multiplicação
		System.out.println( "A multiplicação: "+numero5+" * "+numero6+" = "+resultadoDaMultiplicacao);

		//Saída Divisão
		System.out.println( "A divisão: "+numero7+" / "+numero8+" = "+resultadoDaDivisao);
	}
}